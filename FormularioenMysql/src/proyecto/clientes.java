    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Connection con = null;
        try {
            con = getmysqlconexionbd();
            ps = con.prepareStatement("Select * from clientes where Nombre = ?");
            ps.setString(1, txtNombre.getText());

            rs = ps.executeQuery();

            if (rs.next()) {
                txtidClientes.setText(rs.getString("idClientes"));
                txtNombre.setText(rs.getString("Nombre"));
                txtApellido.setText(rs.getString("Apellido"));
                txtReferencia.setText(rs.getString("Referencia"));
                txtDomicilio.setText(rs.getString("Domicilio"));
                txtPais.setText(rs.getString("Pais"));
                txtCorreo.setText(rs.getString("Correo"));
                txtCedula.setText(rs.getString("Cedula"));
                txtTelefono.setText(rs.getString("Telefono"));
                txtEstadoCivil.setText(rs.getString("EstadoCivil"));
                txtEdad.setText(rs.getString("Edad"));
                cbxSexo.setSelectedItem(rs.getString("Sexo"));
            } else {
                JOptionPane.showMessageDialog(null, "No existe ningun Cliente con ese Nombre");
            }

        } catch (Exception e) {
            System.out.println(e);

        }

    }//GEN-LAST:event_btnBuscarActionPerformed

