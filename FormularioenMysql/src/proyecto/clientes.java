    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jtclientes.setModel(modelo);

            PreparedStatement ps = null;
            ResultSet rs = null;
            mysqlconexionbd conn = new mysqlconexionbd();
            Connection con = conn.getmysqlconexionbd();
            String sql = "Select  Nombre, Apellido, Referencia, Domicilio, Pais, Correo, Cedula, Telefono, EstadoCivil, Edad, Sexo from clientes";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            ResultSetMetaData rsMd = rs.getMetaData();
            int SexoColumnas = rsMd.getColumnCount();

            modelo.addColumn("Nombre");
            modelo.addColumn("Apellido");
            modelo.addColumn("Referencia");
            modelo.addColumn("Domicilio");
            modelo.addColumn("Pais");
            modelo.addColumn("Correo");
            modelo.addColumn("Cedula");
            modelo.addColumn("Telefono");
            modelo.addColumn("EstadoCivil");
            modelo.addColumn("Edad");
            modelo.addColumn("Sexo");

            while (rs.next()) {

                Object[] filas = new Object[SexoColumnas];

                for (int  i= 0; i < SexoColumnas; i++) {

                    filas[i] = rs.getObject(i + 1);

                }
                modelo.addRow(filas);
            }
        } catch (SQLException ex) {

            System.err.println(ex.toString());

        }

    }//GEN-LAST:event_btnCargarActionPerformed

